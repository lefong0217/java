    private void btn_VBR_viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_VBR_viewActionPerformed
        String selectedYear = cbo_VBR_year.getSelectedItem().toString();
        String searchDate = txt_VBR_searchdate.getText().trim();

        // Check if selectedYear or selectedHall is null
        if (selectedYear == null) {
            JOptionPane.showMessageDialog(this, "Please select a year and a hall type.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return; // Exit the method if input is invalid
        }
        
        Manager_File_Operation fileOperation = new Manager_File_Operation();
        List<Manager_BookingRecord> records = fileOperation.fetchBookingRecords(selectedYear, searchDate);

        DefaultTableModel model = (DefaultTableModel) tbl_VBR_bookingrecord.getModel();
        model.setRowCount(0); // Clear existing rows

        for (Manager_BookingRecord record : records) {
            String bookingDate = record.getStartTime().split(" ")[0]; // Extract booking date
            String startTime = record.getStartTime().split(" ")[1]; // Extract start time
            String endTime = record.getEndTime().split(" ")[1]; // Extract end time

            model.addRow(new Object[]{
                bookingDate,
                startTime,
                endTime,
                record.getBookingId(),
                record.getCustomerId(),
                record.getHallId(),
                record.getReservationPeople(),
                record.getTotalAmount()
            });
        }
    }//GEN-LAST:event_btn_VBR_viewActionPerformed

