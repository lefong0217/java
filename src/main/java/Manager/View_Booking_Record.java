    // Method to populate the year combo box based on the Booking file
    private void populateYearComboBox() {
        Set<String> years = new HashSet<>(); // Using Set to avoid duplicate years

        try (BufferedReader br = new BufferedReader(new FileReader("Booking.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(","); 
                if (data.length > 2) { // Ensure StartTime exists
                    String startTime = data[2]; // Assuming StartTime is at index 2
                    String year = startTime.split("-")[0]; // Extract year
                    years.add(year);
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

        // Populate combo box with the years
        cbo_VBR_year.removeAllItems();
        for (String year : years) {
            cbo_VBR_year.addItem(year);
        }
    }


